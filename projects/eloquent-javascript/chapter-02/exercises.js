function triangles(num) {
  for (let i = 1; i <= num; i++) {
    console.log('#'.repeat(i));
  }
}

////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  var num = 1;
  while (num < 16) {
    if (num % 3 === 0 && num % 5 === 0) {
      console.log("fizzbuzz");
    }
    else if (num % 3 === 0) {
      console.log("fizz");
    }
    else if (num % 5 === 0) {
      console.log("buzz");
    }
    else {
      console.log(num);
    }
    num++;
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(squares) {
  let chess = '';
  for (let x = 0; x < squares; x++) {
    for (let y = 0; y < squares; y++) {
      if ((x + y) % 2 === 0) {
        chess += ' ';
      }
      else {
        chess += '#';
      }
    }
    chess += '\n';
  }
  console.log(chess);
}

